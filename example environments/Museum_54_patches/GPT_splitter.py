# ---------- Histogram merge rule (pairwise + chaining) ----------
def apply_histogram_merging_and_emit(builder, ascii_str, p0,u,v,n, bars, collector=None):
    """
    If any bar with height <= 0.5 shares a vertex with any bar with width <= 0.5 * np.sqrt(AREA_THRESHOLD),
    connect them; components (size>=2) get ONE shared material (no grid).
    Others use the ≤ np.sqrt(AREA_THRESHOLD) grid.
    """
    tol = 1e-6
    widths, heights, corners = [], [], []
    for R in bars:
        x0,y0 = R[0]; x1,_ = R[1]; _,yt = R[2]
        widths.append(float(x1 - x0))
        heights.append(float(abs(yt - y0)))
        corners.append([(x0,y0),(x1,y0),(x1,yt),(x0,yt)])
    def share_vertex(i,j):
        for (xi,yi) in corners[i]:
            for (xj,yj) in corners[j]:
                if abs(xi-xj)<=tol and abs(yi-yj)<=tol: return True
        return False
    short  = [h <= 0.5 for h in heights]
    narrow = [w <= 0.5 * np.sqrt(AREA_THRESHOLD) for w in widths]
    nbars = len(bars)
    parent=list(range(nbars))
    def find(a):
        while parent[a]!=a:
            parent[a]=parent[parent[a]]; a=parent[a]
        return a
    def union(a,b):
        ra,rb=find(a),find(b)
        if ra!=rb: parent[rb]=ra
    for i in range(nbars):
        for j in range(i+1, nbars):
            if share_vertex(i,j) and ((short[i] and narrow[j]) or (short[j] and narrow[i])):
                union(i,j)
