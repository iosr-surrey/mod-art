def reindex_materials_to_sequential(builder):
    used_order = [nm for nm in builder.created_mtls if nm in builder.faces and builder.faces[nm]]
    for nm in builder.faces.keys():
        if nm not in used_order:
            used_order.append(nm)
    mapping={}
    new_faces=defaultdict(list)
    next_id=1
    for old in used_order:
        asc = ascii_of(old) or ""
        new_name = f"Patch_{next_id}_Mat_{asc}"
        mapping[old]=new_name
        next_id += 1
    for old, tris in builder.faces.items():
        new_name = mapping.get(old)
        if new_name is None:
            asc = ascii_of(old) or ""
            new_name = f"Patch_{next_id}_Mat_{asc}"
            mapping[old]=new_name
            next_id += 1
        new_faces[new_name].extend(tris)
    builder.faces = new_faces
    return mapping

