def simplify_colinear(P, tol=1e-6):
    n=len(P)
    if n<=3: return P
    keep=[]
    for i in range(n):
        a=P[(i-1)%n]; b=P[i]; c=P[(i+1)%n]
        v1=b-a; v2=c-b
        n1=np.linalg.norm(v1); n2=np.linalg.norm(v2)
        if n1<tol or n2<tol: continue
        cr=abs(v1[0]*v2[1]-v1[1]*v2[0]); dot=v1@v2
        if n1*n2>0 and cr/(n1*n2)<=1e-8 and dot>0: continue
        keep.append(b)
    return np.array(keep,float)

