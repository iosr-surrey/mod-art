def build_adjacency_for_materials(material_names):
    """
    Edge-based adjacency: parts are adjacent iff they share at least one whole edge
    (two common vertex indices) after collapse. This fixes the octagon split case.
    """
    mats = list(material_names)
    edge_to_mats = defaultdict(set)
    # Collect undirected edges per material
    for m in mats:
        for a,b,c in builder.faces.get(m, []):
            for u,v in ((a,b),(b,c),(c,a)):
                if u>v: u,v=v,u
                edge_to_mats[(u,v)].add(m)
    # Build adjacency via shared edges
    adj = {m:set() for m in mats}
    for mats_set in edge_to_mats.values():
        if len(mats_set) >= 2:
            lst = list(mats_set)
            for i in range(len(lst)):
                mi = lst[i]
                for j in range(i+1, len(lst)):
                    mj = lst[j]
                adj[mi].add(mj); adj[mj].add(mi)
    return adj

def dsatur_four_color(adj):
    colors = {}
    nodes = list(adj.keys())
    # Order by degree desc for a good start
    nodes.sort(key=lambda x: -len(adj[x]))
    def smallest_avail(node):
        used = {colors[n] for n in adj[node] if n in colors}
        for c in range(4):
            if c not in used: return c
        return None
    for _ in range(len(nodes)):
        uncolored = [n for n in nodes if n not in colors]
        if not uncolored: break
        def sat(n):
            return len({colors[m] for m in adj[n] if m in colors})
        n_pick = max(uncolored, key=lambda n:(sat(n), len(adj[n])))
        c = smallest_avail(n_pick)
        if c is None:
            # rare fallback
            for c_try in range(4):
                if all(colors.get(nb)!=c_try for nb in adj[n_pick]):
                    c=c_try; break
        if c is None: c=0
        colors[n_pick]=c
    return colors

