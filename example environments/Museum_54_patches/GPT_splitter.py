def hsl_variant(rgb, variant_idx):
    r,g,b = rgb
    # clamp
    r=min(max(r,0.0),1.0); g=min(max(g,0.0),1.0); b=min(max(b,0.0),1.0)
    h,l,s = colorsys.rgb_to_hls(r,g,b)  # HLS in colorsys; L here is "lightness" as in HSL
    if variant_idx == 0:
        l2 = l
    elif variant_idx == 1:
        l2 = l + 0.05
    elif variant_idx == 2:
        l2 = l - 0.05
    elif variant_idx == 3:
        l2 = l + 0.10
    else:
        l2 = l
    l2 = min(max(l2, 0.0), 1.0)
    r2,g2,b2 = colorsys.hls_to_rgb(h, l2, s)
    return (r2,g2,b2)

def recolor_props(props_lines, rgb_out):
    r,g,b = rgb_out
    out=[]
    kd_done=False; ka_done=False; ks_done=False
    for ln in props_lines:
        s = ln.strip().lower()
        if s.startswith("kd "):
            out.append(f"Kd {r:.6f} {g:.6f} {b:.6f}\n"); kd_done=True
        elif s.startswith("ka "):
            out.append(f"Ka {r:.6f} {g:.6f} {b:.6f}\n"); ka_done=True
        elif s.startswith("ks "):
            out.append(f"Ks {r:.6f} {g:.6f} {b:.6f}\n"); ks_done=True
        else:
            out.append(ln if ln.endswith("\n") else ln+"\n")
    if not kd_done: out.append(f"Kd {r:.6f} {g:.6f} {b:.6f}\n")
    if not ka_done: out.append(f"Ka {r:.6f} {g:.6f} {b:.6f}\n")
    if not ks_done: out.append(f"Ks {r:.6f} {g:.6f} {b:.6f}\n")
    return out

